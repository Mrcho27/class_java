package variable;

public class Ex01 {
	public static void main(String[] args) {
		int a;  // 정수를 저장할 수 잇는 변수 a를 선언했다.(만들었다)
		a = 3; //변수 a에 3이라는 정수 값을 저장했다.
		
		int b = 5; //정수를 저장할 수 있는 변수 b를 선언하고 5라는 정수 값으로 초기화 하였다.
		
//	변수에 저장된 값을 사용하는 방법
//	변수명을 이용한다.
		System.out.println(a);
		
		int c, d = 3, e = 5;
	}
}
